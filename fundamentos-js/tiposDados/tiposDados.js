// tipos de dados number

//tipos numéricos possiveis
let num = 3
const num2 = 3.5
const num3 = 3.1415
const num4 = 3
const num5 = .5

//operações matemáticas	
//soma
console.log(num + num2)

//subtração
console.log(num - num3)

//multiplicação
console.log(num3 * num2)

//divisão
console.log(num4 / num)

//exponenciação
console.log(num ** num2)

//módulo
console.log(num % num2)

//incremento
console.log(num++)

//decremento
console.log(num--)

//NaN - Not a Number
console.log(7 / 'oi')

